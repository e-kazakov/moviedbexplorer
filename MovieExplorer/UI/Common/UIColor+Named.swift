import UIKit

extension UIColor {
  
  static var appBackground: UIColor {
    return UIColor(named: "AppBackground")!
  }
  
  static var appSecondaryBackground: UIColor {
    return UIColor(named: "AppSecondaryBackground")!
  }
  
  static var appLabel: UIColor {
    return UIColor(named: "AppLabel")!
  }
  
  static var appSeparator: UIColor {
    return UIColor(named: "AppSeparator")!
  }
  
  static var appSkeleton: UIColor {
    return UIColor(named: "AppSkeleton")!
  }
  
}
